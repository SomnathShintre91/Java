import java.util.Scanner;

public class Switch_case_2 {
    public static void main(String[] args) {
        // defining a variable
        char ch;
        // Taking input from user
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter lower case alphabet: ");
        // Character input
        ch = sc.next().charAt(0);
        sc.close();

        switch (ch) {
            case 'a':
                System.out.println("Vowel");
                break;
            case 'e':
                System.out.println("Vowel");
                break;
            case 'i':
                System.out.println("Vowel");
                break;
            case 'o':
                System.out.println("Vowel");
                break;
            case 'u':
                System.out.println("Vowel");
                break;
            case 'A':
                System.out.println("Vowel");
                break;
            case 'E':
                System.out.println("Vowel");
                break;
            case 'I':
                System.out.println("Vowel");
                break;
            case 'O':
                System.out.println("Vowel");
                break;
            case 'U':
                System.out.println("Vowel");
                break;
            default:
                System.out.println("Consonant");
        }
    }
}